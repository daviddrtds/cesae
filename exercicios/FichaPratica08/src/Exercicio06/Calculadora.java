package Exercicio06;

public class Calculadora {


    public Calculadora() {
    }

    public double adicao(int num1, int num2) {
        return num1 + num2;
    }

    public double subtracao(int num1, int num2) {
        return num1 - num2;
    }

    public double multiplicacao(int num1, int num2) {
        return num1 * num2;
    }

    public double divisao(double num1, double num2) {
        return num1 / num2;
    }
}
