package SerVivo;

public class SerVivo {

    private String nome;
    private String especie;
    private String paisOrigem;
    private int idade;


    public SerVivo(String nome, String especie, String paisOrigem, int idade) {
        this.nome = nome;
        this.especie = especie;
        this.paisOrigem = paisOrigem;
        this.idade = idade;
    }
}
